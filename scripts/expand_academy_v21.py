def validate_lesson(lesson):
    required = ('id', 'code', 'title', 'summary', 'longExplanation', 'questions')
    missing = [key for key in required if key not in lesson]
    if missing:
        raise ValueError(f"Lesson {lesson.get('id')} missing: {missing}")
    for item in lesson['questions']:
        options = item.get('options', [])
        answer = item.get('answer')
        if len(options) != 4 or not isinstance(answer, int) or not 0 <= answer < 4:
            raise ValueError(f"Invalid question in {lesson['id']}: {item.get('q')}")


